# Inicializa a variável area com 0
area = 0

# Solicita ao usuário para fornecer o raio do círculo
raio = float(input("Qual o valor do raio do círculo?: "))

# Calcula a área do círculo usando a fórmula: area = π * raio^2
area = 3.14 * raio ** 2

# Exibe o resultado
print("A área do círculo é:", area)

